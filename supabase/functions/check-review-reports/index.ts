import "jsr:@supabase/functions-js/edge-runtime.d.ts"
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const RESEND_API_KEY = Deno.env.get('RESEND_API_KEY');

serve(async (req) => {
  const { record } = await req.json();

  let mailResData = { messeage: 'no need to send mail'};
  
  if (record.count >= 5) {
    const res = await fetch('https://api.resend.com/emails', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${RESEND_API_KEY}`,
      },
      body: JSON.stringify({
        from: 'team@klavhub.com',
        to: 'esterpratt@gmail.com',
        subject: 'Review was reported more than 5 times',
        html: `The review: ${record.review_id} was reported: ${record.count} times`,
      }),
    });

    mailResData = await res.json();
  }

  return new Response(JSON.stringify(mailResData), {
      status: 200,
      headers: {
        'Content-Type': 'application/json',
      },
    })
})

